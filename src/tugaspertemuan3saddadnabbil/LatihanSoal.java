    private void HapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusButtonActionPerformed
        // TODO add your handling code here:
        nim.setText("");
        nama.setText("");
        jeniskelamin.clearSelection(); 
        jurusan.setSelectedIndex(-1);
        semester.setText("");
        biaya.setText("");
        nilkehadiran.setText("");
        niltugas.setText("");
        niluts.setText("");
        niluas.setText("");
        output.setText("");
        
        DefaultTableModel dataModel = (DefaultTableModel) rangkumanNilaiTable.getModel();    
        if (rangkumanNilaiTable.getRowCount() > 0) {
            for (int i = rangkumanNilaiTable.getRowCount() - 1; i > -1; i--) {
                dataModel.removeRow(i);
            }
        }
    }//GEN-LAST:event_HapusButtonActionPerformed

    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = rangkumanNilaiTable.getSelectedRow();
        if (selectedRow == -1) { // Jika tidak ada baris yang dipilih
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diperbarui.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return; // Keluar dari method untuk menghentikan pembaruan
        }
        if (selectedRow != -1) { // Pastikan ada baris yang dipilih
            DefaultTableModel model = (DefaultTableModel) rangkumanNilaiTable.getModel();
            model.setValueAt(nim.getText(), selectedRow, 0);
            model.setValueAt(nama.getText(), selectedRow, 1);
            // Mengambil nilai dari radio button jenis kelamin
            String selectedGender = "";
            if (lakiRadioButton.isSelected()) {
                selectedGender = "Laki-laki";
            } else if (perempuanRadioButton.isSelected()) {
                selectedGender = "Perempuan";
            }
            model.setValueAt(selectedGender, selectedRow, 2);
            model.setValueAt(semester.getText(), selectedRow, 3);
            model.setValueAt(jurusan.getSelectedItem().toString(), selectedRow, 4);
            model.setValueAt(biaya.getText(), selectedRow, 5);
            model.setValueAt(nilaiPemrograman2.getText(), selectedRow, 6);
            model.setValueAt(grade.getText(), selectedRow, 7);
            
            // Set teks output dengan nilai yang diperbarui
            output.setText(
                "NIM \t: " + nim.getText() +
                "\nNama\t: " + nama.getText() +
                "\nJenis Kelamin\t: " + selectedGender +
                "\nJurusan\t: " + jurusan.getSelectedItem().toString() +
                "\nSemester\t: " + semester.getText() +
                "\nBiaya\t: " + biaya.getText()
            );
    
            // Tampilkan pesan berhasil
            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        }
        
        
    }//GEN-LAST:event_UpdateButtonActionPerformed

    private void rangkumanNilaiTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rangkumanNilaiTableMouseClicked
        // TODO add your handling code here:
        int selectedRow = rangkumanNilaiTable.getSelectedRow();
        if (selectedRow != -1) { // -1 berarti tidak ada baris yang dipilih
            DefaultTableModel model = (DefaultTableModel) rangkumanNilaiTable.getModel();
            nim.setText(model.getValueAt(selectedRow, 0).toString());
            nama.setText(model.getValueAt(selectedRow, 1).toString());
            String gender = model.getValueAt(selectedRow, 2).toString();
            // Untuk memilih radio button berdasarkan jenis kelamin
            if (gender.equals("Laki-laki")) {
                lakiRadioButton.setSelected(true);
            } else if (gender.equals("Perempuan")) {
                perempuanRadioButton.setSelected(true);
            }
            semester.setText(model.getValueAt(selectedRow, 3).toString());
            jurusan.setSelectedItem(model.getValueAt(selectedRow, 4).toString());
            biaya.setText(model.getValueAt(selectedRow, 5).toString());
            nilaiPemrograman2.setText(model.getValueAt(selectedRow, 6).toString());
            grade.setText(model.getValueAt(selectedRow, 7).toString());
            // Mengosongkan text field yang tidak ada dalam tabel
            nilkehadiran.setText("");
            niltugas.setText("");
            niluts.setText("");
            niluas.setText("");
            output.setText("");
        }
    }//GEN-LAST:event_rangkumanNilaiTableMouseClicked


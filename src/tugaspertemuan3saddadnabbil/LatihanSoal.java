    private void HitungButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HitungButtonActionPerformed
        // TODO add your handling code here:
        String selectedGender = "";
        Enumeration<AbstractButton> genders = jeniskelamin.getElements();
        while (genders.hasMoreElements()) {
            AbstractButton genderButton = genders.nextElement();
            if (genderButton.isSelected()) {
                selectedGender = genderButton.getText();
                break;
            }
        }
        
        String selectedJurusan = "";
        if (jurusan.getSelectedItem() != null) {
            selectedJurusan = jurusan.getSelectedItem().toString();
            switch (selectedJurusan) {
                case "Teknik Informatika":
                    biaya.setText("300000");
                    break;
                case "Ekonomi":
                    biaya.setText("200000");
                    break;
                case "MIPA":
                    biaya.setText("225000");
                    break;
                default:
                    // Handle default case
                    break;
            }
        }

        int kehadiran, tugas, uts, uas;
        double pemrograman;

        kehadiran = Integer.parseInt(nilkehadiran.getText());
        tugas = Integer.parseInt(niltugas.getText());
        uts = Integer.parseInt(niluts.getText());
        uas = Integer.parseInt(niluas.getText());
        
        pemrograman = 0.1 * kehadiran + 0.2 * tugas + 0.3 * uts + 0.4 * uas;
        
        String gradePemrograman;
        if (pemrograman < 50) {
            gradePemrograman = "E";
        } else if (pemrograman < 60) {
            gradePemrograman = "D";
        } else if (pemrograman < 70) {
            gradePemrograman = "C";
        } else if (pemrograman < 80) {
            gradePemrograman = "B";
        } else {
            gradePemrograman = "A";
        }
        
        if (jurusan.getSelectedItem() != null) {
        selectedJurusan = jurusan.getSelectedItem().toString();
            switch (selectedJurusan) {
                case "Teknik Informatika":
                    biaya.setText("300000");
                    break;
                case "Ekonomi":
                    biaya.setText("200000");
                    break;
                case "MIPA":
                    biaya.setText("225000");
                    break;
                default:
                    biaya.setText("");
                    break;
            }
        }
        
        output.setText(
            "NIM \t: " + nim.getText() +
            "\nNama\t: " + nama.getText() +
            "\nJenis Kelamin\t: " + selectedGender +
            "\nJurusan\t: " + selectedJurusan +   
            "\nSemester\t: " + semester.getText() +
            "\nBiaya\t: " + biaya.getText()
        );
        
        DefaultTableModel model = (DefaultTableModel) rangkumanNilaiTable.getModel();
        String[] rowData = {
            nim.getText(),
            nama.getText(),
            selectedGender,
            semester.getText(),
            selectedJurusan,
            biaya.getText(),
            String.valueOf(pemrograman), 
            gradePemrograman
        };
        model.addRow(rowData);
    }//GEN-LAST:event_HitungButtonActionPerformed

